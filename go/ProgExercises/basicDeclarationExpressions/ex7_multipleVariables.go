package main

import (
	"fmt"
)

var a int = 125
var b int = 12345
var ax int = 1234567890
var s int = 4043
var x float64 = 2.13459
var dx float64 = 1.1415927
var c string = "W"
var ux int = 2541567890

func main() {
	fmt.Printf("int a: %d \n", a)
	fmt.Printf("int b: %d \n", b)
	fmt.Printf("int ax: %d \n", ax)
	fmt.Printf("float64 x: %g \n", x)
	fmt.Printf("float64 dx: %g \n", dx)
	fmt.Printf("string c %s \n", c)
	fmt.Printf("int ux %d \n ", ux)
}
